#include "../push_swap.h"

int	dup_push(char *s, t_node **a)
{
	long long	num;

	num = ft_atoi(s);
	if (num > 2147483647 || num < -2147483648)
		return (-1);
	push(a, num, 0, 0);
	return (0);
}
